    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try{
            String nom =txtnom.getText();
            String prenom =txtprenom.getText();                      
            String tel=txttel.getText().trim();
            if(!tel.matches("\\d{9,15}")){
                JOptionPane.showMessageDialog(this,"numero invalide ");
                return;
            }
            String addresse =txtaddresse.getSelectedItem().toString();            
            Client client =new Client(nom,prenom,tel,addresse);          
            db.inserclient(client);
            chargerpage();
            vidertexclient();
            JOptionPane.showMessageDialog(this,"Enregistrement reussie" );            
        } catch (SQLException ex) {
            System.getLogger(InterfaceClient.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            JOptionPane.showMessageDialog(this,"Erreur d'insertion " + ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String id = txtid.getText().trim();
            if (id.isEmpty()) {
           JOptionPane.showMessageDialog(null, "Veuillez selectionner une champ du tableau");
           return;
       }
           int id_selectionne = Integer.parseInt(id);   
           try {
               db.deleteclient(id_selectionne);
           } catch (SQLException ex) {
               System.getLogger(InterfaceClient.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
           }
           vidertexclient();          
            chargerpage();
    }//GEN-LAST:event_jButton6ActionPerformed

